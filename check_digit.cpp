            break;
        case 3:
            cout<<"Tree";
            break;
        case 4:
            cout<<"Foure";
            break;
        case 5:
            cout<<"Five";
            break;
        case 6:
            cout<<"six";
            break;
        case 7:
            cout<<"seven";
            break;
        case 8:
            cout<<"Eight";
            break;
        case 9:
            cout<<"Nine";
            break;
        
        default:
